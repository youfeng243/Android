package com.example.server;

import android.annotation.SuppressLint;
import android.content.Context;
import android.graphics.Bitmap;
import android.graphics.Canvas;
import android.graphics.Color;
import android.graphics.Paint;
import android.util.DisplayMetrics;
import android.view.MotionEvent;
import android.view.View;

public class DrawView extends View {

	public Canvas canvas;
	public Paint p;
	private Bitmap bitmap;
	float x,y;
	int bgColor;
	
	public DrawView(Context context) {
		super(context);
		
		//屏幕信息
		DisplayMetrics dm2 = context.getResources().getDisplayMetrics();
		//m_ScreenWidth = dm2.widthPixels;
		//m_ScreenHeight = dm2.heightPixels;
		//int[]  colors={Color.WHITE,Color.BLACK,Color.BLUE};
		bitmap = Bitmap.createBitmap(dm2.widthPixels, dm2.heightPixels, Bitmap.Config.ARGB_8888);    //设置位图，线就画在位图上面，第一二个参数是位图宽和高
		//bitmap.
		canvas=new Canvas();         
		canvas.setBitmap(bitmap);       
		p = new Paint(Paint.DITHER_FLAG);
		p.setAntiAlias(true);                //设置抗锯齿，一般设为true
		p.setColor(Color.RED);              //设置线的颜色
		p.setStrokeCap(Paint.Cap.ROUND);     //设置线的类型
		p.setStrokeWidth(8);                //设置线的宽度
		
		//canvas.drawLine(0, 100, 100, 100, p);
		//canvas.drawLine(100, 0, 100, 100, p);
	}
	
	//触摸事件
	@SuppressLint("ClickableViewAccessibility")
	@Override
	public boolean onTouchEvent(MotionEvent event) {

		if (event.getAction() == MotionEvent.ACTION_MOVE) {    //拖动屏幕
			canvas.drawLine(x, y, event.getX(), event.getY(), p);   //画线，x，y是上次的坐标，event.getX(), event.getY()是当前坐标
			invalidate();
		}

		if (event.getAction() == MotionEvent.ACTION_DOWN) {    //按下屏幕
			x = event.getX();				
			y = event.getY();
			canvas.drawPoint(x, y, p);                //画点
			invalidate();
		}
		if (event.getAction() == MotionEvent.ACTION_UP) {    //松开屏幕
		
		}
		x = event.getX();   //记录坐标
		y = event.getY();
		return true;
	}
	
	@Override
	public void onDraw(Canvas c) {			    		
		c.drawBitmap(bitmap, 0, 0, null);	      
	}

}
